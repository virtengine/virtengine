/**
 * Bottom navigation bar for mobile devices.
 * Rendered only on mobile via parent layout visibility classes.
 */
export function MobileBottomNav() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed inset-x-0 bottom-0 z-50 border-t border-border bg-background/95 backdrop-blur supports-[backdrop-filter]:bg-background/80 md:hidden"
      aria-label="Mobile navigation"
    >
      <ul className="flex items-center justify-around">
        {navItems.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + '/');

          return (
            <li key={item.name} className="flex-1">
              <Link
                href={item.href}
                className={`flex flex-col items-center gap-1 px-2 py-3 text-2xs transition-colors ${
                  isActive ? 'text-primary' : 'text-muted-foreground active:text-foreground'
                }`}
                aria-current={isActive ? 'page' : undefined}
              >
                {item.icon}
                <span className="leading-none">{item.name}</span>
              </Link>
            </li>
          );
        })}
      </ul>
    </nav>
  );
}
